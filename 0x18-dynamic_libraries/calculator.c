#include <stdio.h>
/**
*add - takes two numbers and returns sum as an integer.
*
*mul - take two integers and returns as product
*
*div - take two integer and return a division of the integers
*
*mod - takes two num and returns a modulus
*
*sub - takes two num and give the difference
*@a: integer
*@b: integer
* Return: value
*/

int add(int a, int b)
{
return (a + b);
}

int mul(int a, int b)
{
return (a * b);
}

int div(int a, int b)
{
return (a / b);
}

int mod(int a, int b)
{
return (a % b);
}

int sub(int a, int b)
{
return (a - b);
}
